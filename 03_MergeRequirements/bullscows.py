def bullscows(guess: str, secret: str) -> (int, int):
    assert len(guess) == len(secret), 'Wrong len'

    bulls = sum(map(lambda a, b: a == b, guess, secret))
    cows = len(set(guess).intersection(set(secret)))

    return bulls, cows


def ask(prompt: str, valid: list[str] = None) -> str:
    while True:
        cow_print(prompt)
        guess = input()
        if valid is not None:
            if guess not in valid:
                continue
        break
    return guess


def inform(format_string: str, bulls: int, cows: int) -> None:
    cow_print(format_string.format(bulls, cows))


def gameplay(ask: callable, inform: callable, words: list[str]) -> int:
    secret = random.choice(words)
    print(secret)
    counter = 0

    while True:
        guess = ask("Введите слово: ", words)
        counter += 1
        bulls, cows = bullscows(guess, secret)
        inform("Быки: {}, Коровы: {}", bulls, cows)
        if guess == secret:
            # print(f'Количество попыток: {counter}')
            break
    return counter


